#include <stdio.h>

int main() {
    int x = 12, y = 7, z = 12; // กำหนดค่าเริ่มต้นให้ตัวแปร x, y, z

    // 1. x > y คือ 12 > 7 → จริง → ผลลัพธ์ = 1
    printf("1. x > y        = %d\n", x > y);

    // 2. x < z คือ 12 < 12 → เท็จ → ผลลัพธ์ = 0
    printf("2. x < z        = %d\n", x < z);

    // 3. x == z คือ 12 เท่ากับ 12 → จริง → ผลลัพธ์ = 1
    printf("3. x == z       = %d\n", x == z);

    // 4. x != y คือ 12 ไม่เท่ากับ 7 → จริง → ผลลัพธ์ = 1
    printf("4. x != y       = %d\n", x != y);

    // 5. !(2*5 >= y) || (5 != (5/3))
    //   2*5 = 10 → 10 >= 7 → จริง → !(จริง) = เท็จ (0)
    //   5/3 (เลขจำนวนเต็ม) = 1 → 5 != 1 → จริง (1)
    //   เท็จ || จริง = จริง → ผลลัพธ์ = 1
    printf("5. !(25 >= y) || (5 != (5/3)) = %d\n", !(25 >= y) || (5 != (5/3)));

    // 6. !(x < y) คือ !(12 < 7) → !(เท็จ) = จริง → ผลลัพธ์ = 1
    printf("6. !(x < y)     = %d\n", !(x < y));

    // 7. (x + y) * (z * 2)
    //    (12 + 7) * (12 * 2) → 19 * 24 = 456
    printf("7. (x + y) * (z * 2) = %d\n", (x + y) * (z * 2));

    // 8. (x % 2 == 0) || (y % 2 == 1)
    //    12 % 2 = 0 → จริง, 7 % 2 = 1 → จริง → จริง || จริง = จริง → ผลลัพธ์ = 1
    printf("8. (x %% 2 == 0) || (y %% 2 == 1) = %d\n", (x % 2 == 0) || (y % 2 == 1));

    // 9. (x > y) && (z < y)
    //    12 > 7 → จริง, 12 < 7 → เท็จ → จริง && เท็จ = เท็จ → ผลลัพธ์ = 0
    printf("9. (x > y) && (z < y) = %d\n", (x > y) && (z < y));

    return 0;
}